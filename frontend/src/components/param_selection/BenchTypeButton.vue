<!--
  @fileoverview BenchTypeButton component that allows users to select how to configure the benchmark.
  @component BenchTypeButton
  @author Eva Ray
  @description This component provides a button group for selecting the way to configure the benchmark query, such as
  using a config file, a unique query with parameters selection, or a workload with parameters selection. It emits the
  selected type to the parent component.
  The style has been generated by an LLM.
-->

<script setup>

import { ref } from 'vue'

const benchChoice = ref('UNIQUE')

const choices = [
    { label: 'Config file', value: 'CONFIG' },
    { label: 'Unique query', value: 'UNIQUE' },
    { label: 'Workload', value: 'WORKLOAD' }
]

// Expose data to parent component
defineExpose({
    getSelections: () => {
        return {
            benchType: benchChoice.value
        }
    }
})

</script>

<template>
        <div class="btn-group">
            <label v-for="choice in choices" :key="choice.value" class="btn-toggle" :class="{ 'active': benchChoice === choice.value }">
                <input type="radio" :value="choice.value" v-model="benchChoice" />
                <span>{{ choice.label }}</span>
            </label>
        </div>
</template>

