<!--
  @fileoverview DbSizeSelection component that allows users to select a database size for benchmarking.
  @component DbSizeSelection
  @author Eva Ray
  @description This component provides a selection interface for different database sizes, such as SMALL, MEDIUM, and LARGE.
  It fetches the list of sizes from a server endpoint and allows users to select one size. It emits the selected size
  value to the parent component. The style has been generated by an LLM.
-->

<script setup>
import { ref, onMounted } from 'vue'

const sizes = ref([])
const selectedSizeValue = ref('')

// Fetch available database sizes from the server
onMounted(async () => {
    try {
        const response = await fetch('http://localhost:7070/meta/sizes')
        if (!response.ok) throw new Error('Network')
        sizes.value = await response.json()

        // Set default selection to MEDIUM if it exists
        const mediumSize = sizes.value.find(size => size.value === 'MEDIUM')
        if (mediumSize) {
            selectedSizeValue.value = mediumSize.value
        }
    } catch (error) {
        console.error('Error loading db sizes:', error)
    }
})

// Expose data to parent component
defineExpose({
    getSelections: () => {
        const selected = sizes.value.find(s => s.value === selectedSizeValue.value)
        return {
            selectedSize: selected ? selected.value : null
        }
    }
})
</script>

<template>
    <div class="container">
        <h2>Database Size</h2>
        <div class="selection-row">
            <label v-for="size in sizes" :key="size.label" class="radio-label">
                <input type="radio" :value="size.value" v-model="selectedSizeValue" class="radio-input" />
                <span class="radio-text">{{ size.label }}</span>
            </label>
        </div>
    </div>
</template>

