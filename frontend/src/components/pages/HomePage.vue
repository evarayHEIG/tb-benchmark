<!--
  @fileoverview HomePage component that allows users to select parameters for benchmarking and navigate to results.
  @component HomePage
  @author Eva Ray
  @description This component serves as the landing page for the application, providing a user interface for selecting
  benchmark parameters. The style has been generated by an LLM.
-->

<template>
  <div class="home-page">
    <ParametersSelection @results-ready="handleResultsReady" />
  </div>
</template>

<script setup>
import { inject } from 'vue'
import ParametersSelection from '../param_selection/ParametersSelection.vue'

const setBenchmarkResults = inject('setBenchmarkResults')

/*
  @description Callback function to handle when benchmark results are ready.
  @param {Object} results - The benchmark results to be set.
*/
const handleResultsReady = (results) => {
  setBenchmarkResults(results)
  window.location.hash = '#/results'
}
</script>

<style scoped>
.home-page {
  min-height: 100vh;
}
</style>
